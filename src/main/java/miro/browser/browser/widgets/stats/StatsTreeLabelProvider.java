package main.java.miro.browser.browser.widgets.stats;

import org.eclipse.jface.viewers.ILabelProviderListener;
import org.eclipse.jface.viewers.ITableLabelProvider;
import org.eclipse.swt.graphics.Image;

public class StatsTreeLabelProvider implements ITableLabelProvider {

	StatsViewerType type;
	
	public StatsTreeLabelProvider(StatsViewerType type) {
		this.type = type;
	}
	
	
	@Override
	public void addListener(ILabelProviderListener listener) {
		// TODO Auto-generated method stub

	}

	@Override
	public void dispose() {
		// TODO Auto-generated method stub

	}

	@Override
	public boolean isLabelProperty(Object element, String property) {
		// TODO Auto-generated method stub
		return false;
	}

	@Override
	public void removeListener(ILabelProviderListener listener) {
		// TODO Auto-generated method stub

	}

	@Override
	public Image getColumnImage(Object element, int columnIndex) {
		// TODO Auto-generated method stub
		return null;
	}

	@Override
	public String getColumnText(Object element, int columnIndex) {
		StatsTreeItem item = (StatsTreeItem) element;
		
		
		if(columnIndex == 1){
			return item.getKey();
		}
		if(columnIndex == 2){
			return Integer.toString(item.getAmount());
		}
		
		if(columnIndex == 3){
			return Float.toString((int) (item.getFraction()*100)) + "%";
		}
		return null;
	}

}
